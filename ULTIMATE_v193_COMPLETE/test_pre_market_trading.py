def live_gap_analysis(execute: bool = False, market: str = None):
    """
    Analyze gap predictions from actual overnight pipeline reports
    
    Args:
        execute: If True, execute trades based on gap signals
        market: Specific market to analyze ('au', 'uk') or None for all
    """
    logger.info("\n" + "="*80)
    logger.info("LIVE PRE-MARKET GAP ANALYSIS")
    logger.info("="*80)
    
    # Check for overnight reports
    report_dir = Path(__file__).parent / 'reports' / 'screening'
    
    markets_to_check = [market] if market else ['au', 'uk']
    reports_found = {}
    
    for mkt in markets_to_check:
        report_path = report_dir / f'{mkt}_morning_report.json'
        if report_path.exists():
            try:
                with open(report_path, 'r') as f:
                    report = json.load(f)
                reports_found[mkt] = report
                logger.info(f"[OK] Loaded {mkt.upper()} morning report from {report_path}")
            except Exception as e:
                logger.error(f"[ERROR] Failed to load {mkt.upper()} report: {e}")
        else:
            logger.warning(f"[WARN] No {mkt.upper()} report found at {report_path}")
    
    if not reports_found:
        logger.error("No overnight reports found. Run the overnight pipelines first:")
        logger.error("  python scripts/run_au_full_pipeline.py")
        logger.error("  python scripts/run_uk_full_pipeline.py")
        return
    
    # Initialize paper trading coordinator if executing trades
    if execute:
        logger.info("\n[EXECUTE] Initializing paper trading coordinator...")
        
        # Get symbols from all reports
        all_symbols = []
        for report in reports_found.values():
            for stock in report.get('top_opportunities', report.get('top_stocks', [])):
                all_symbols.append(stock['symbol'])
        
        coordinator = PaperTradingCoordinator(
            symbols=all_symbols,
            initial_capital=100000.0,
            use_enhanced_adapter=True
        )
        
        # Run pre-market gap analysis with trade execution
        results = coordinator.analyze_pre_market_gaps(execute_trades=True)
        
        logger.info("\n" + "="*80)
        logger.info("EXECUTION RESULTS:")
        logger.info("="*80)
        
        for mkt, decision in results.items():
            logger.info(f"\n{mkt.upper()}:")
            logger.info(f"  Entered: {decision['should_enter']}")
            if decision['should_enter']:
                logger.info(f"  Positions: {len(decision['recommended_symbols'])}")
                logger.info(f"  Position Size: {decision['position_multiplier']:.2f}x")
        
        # Show current positions
        logger.info("\n" + "-"*80)
        logger.info("CURRENT POSITIONS:")
        logger.info("-"*80)
        if coordinator.positions:
            for symbol, pos in coordinator.positions.items():
                logger.info(f"  {symbol}: {pos.shares} shares @ ${pos.entry_price:.2f}")
        else:
            logger.info("  None")
    
    else:
        # Analysis only (no execution)
        strategy = PreMarketStrategy()
        
        for mkt, report in reports_found.items():
            market_sentiment = report.get('market_sentiment', {})
            gap_prediction = market_sentiment.get('gap_prediction', {})
            
            if not gap_prediction or 'predicted_gap_pct' not in gap_prediction:
                logger.warning(f"[WARN] No gap prediction in {mkt.upper()} report")
                continue
            
            sentiment_score = market_sentiment.get('sentiment_score', 50)
            world_risk = report.get('world_risk_score', 50)
            top_stocks = report.get('top_opportunities', report.get('top_stocks', []))
            
            decision = strategy.analyze_gap_opportunity(
                market=mkt,
                gap_prediction=gap_prediction,
                world_risk_score=world_risk,
                sentiment_score=sentiment_score,
                top_stocks=top_stocks
            )
            
            logger.info(f"\n{mkt.upper()} DECISION:")
            logger.info(f"  Should Enter: {decision['should_enter']}")
            if decision['should_enter']:
                logger.info(f"  Position Size: {decision['position_multiplier']:.2f}x")
                logger.info(f"  Risk: {decision['risk_level']}")
                logger.info(f"  Timing: {decision['timing']}")
                logger.info(f"  Symbols: {', '.join(decision['recommended_symbols'])}")
    
    logger.info("\n" + "="*80)
    logger.info("ANALYSIS COMPLETE")
    logger.info("="*80)


def main():
    parser = argparse.ArgumentParser(description='Pre-Market Gap Trading Demo')
    parser.add_argument('--demo', action='store_true', help='Run demo with sample data')
    parser.add_argument('--execute', action='store_true', help='Execute trades (requires overnight reports)')
    parser.add_argument('--market', type=str, choices=['au', 'uk'], help='Specific market to analyze')
    
    args = parser.parse_args()
    
    if not MODULES_AVAILABLE:
        logger.error("Required modules not available. Cannot run demo.")
        return 1
    
    # Create logs directory
    Path('logs').mkdir(exist_ok=True)
    
    if args.demo:
        demo_gap_analysis()
    else:
        live_gap_analysis(execute=args.execute, market=args.market)
    
    return 0


if __name__ == '__main__':
    sys.exit(main())
