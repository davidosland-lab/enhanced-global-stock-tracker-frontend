"""
Pre-Market Gap Trading Demo
============================
VERSION: v1.3.15.193.11.6.10

This script demonstrates the new pre-market gap prediction trading feature.

It shows how to:
1. Load gap predictions from overnight pipeline reports (AU/UK)
2. Analyze gap opportunities with risk assessment
3. Calculate position sizing based on gap confidence
4. Execute pre-market trades based on gap signals

Gap predictions are generated by:
- AU: SPI 200 proxy (pipelines/models/screening/spi_proxy_realtime.py)
- UK: FTSE 100 proxy (pipelines/models/screening/ftse_proxy_realtime.py)

These predictors use:
- US market closes (S&P 500, NASDAQ, DOW)
- Overnight futures (ES, NQ for AU; Z=F for UK)
- Currency impacts (AUD/USD, GBP/USD)
- Commodity prices (Oil, Iron Ore)
- VIX for confidence adjustment

USAGE:
    # Demo only (no actual trades)
    python test_pre_market_trading.py --demo
    
    # Live execution (requires running overnight pipeline first)
    python test_pre_market_trading.py --execute
    
    # Specific market only
    python test_pre_market_trading.py --market uk --execute
"""

